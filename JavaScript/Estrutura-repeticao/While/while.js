let contador = 0;
let multiplicador = 6;

console.log(`Tabuada do ${multiplicador } \n`);

while(contador <= 10){ //Condição da estruta de repetição while
    
   
   //O valor da multiplicação entre os números é guardado na variável resultado
    let soma = contador * multiplicador;                           
    console.log(`${multiplicador} X ${contador} = ${soma}`) // Impressão das variaveis
    contador++ //Incrimento do contador                                         
                                                    
}
